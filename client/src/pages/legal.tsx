export default function LegalPage({ doc }: { doc: "oferta" | "privacy" | "terms" }) {
  const [, setLocation] = useLocation();
  const legal = LEGAL_DOCS[doc];

  if (!legal) return null;

  const renderMarkdown = (text: string) => {
    const lines = text.trim().split("\n");
    const elements: React.ReactNode[] = [];
    let key = 0;

    for (const line of lines) {
      if (line.startsWith("## ")) {
        elements.push(
          <h2 key={key++} style={{ fontSize: "1.2rem", fontWeight: 700, marginTop: "2rem", marginBottom: "0.75rem", color: "#1D1D1F" }}>
            {line.slice(3)}
          </h2>
        );
      } else if (line.startsWith("- ")) {
        elements.push(
          <li key={key++} style={{ marginLeft: "1.5rem", marginBottom: "0.3rem", color: "#444" }}>
            {line.slice(2)}
          </li>
        );
      } else if (line.trim() === "") {
        elements.push(<div key={key++} style={{ height: "0.4rem" }} />);
      } else {
        elements.push(
          <p key={key++} style={{ marginBottom: "0.4rem", color: "#444", lineHeight: 1.7 }}>
            {line}
          </p>
        );
      }
    }
    return elements;
  };

  return (
    <div style={{ minHeight: "100vh", background: "#FBFBFD", fontFamily: "-apple-system, BlinkMacSystemFont, 'SF Pro Text', sans-serif" }}>
      <div style={{ maxWidth: 760, margin: "0 auto", padding: "4rem 2rem" }}>
        <button
          onClick={() => setLocation("/")}
          style={{ display: "flex", alignItems: "center", gap: "0.5rem", background: "none", border: "none", cursor: "pointer", color: "#86868B", fontSize: "0.9rem", marginBottom: "2.5rem", padding: 0 }}
        >
          <ArrowLeft size={16} />
          На главную
        </button>
        <h1 style={{ fontSize: "clamp(1.8rem, 4vw, 2.5rem)", fontWeight: 700, letterSpacing: "-0.03em", marginBottom: "2rem", color: "#1D1D1F" }}>
          {legal.title}
        </h1>
        <div style={{ lineHeight: 1.7, fontSize: "0.95rem" }}>
          {renderMarkdown(legal.content)}
        </div>
      </div>
    </div>
  );
}
